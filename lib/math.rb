def addition(num1, num2)
  sum = num1 + num2
  puts sum
  sum
end

def subtraction(num1, num2)
  difference = num1 - num2
  puts difference
  difference
end

def division(num1, num2)
  quotient = num1 / num2
  puts quotient
  quotient
end

def multiplication(num1, num2)
  product = num1 * num2
  puts product
  product
end

def modulo(num1, num2)
  quotient = num1 / num2
  mod = num1 - quotient * num2
  puts mod
  mod
end

def square_root(num)
  Math.sqrt(num)
end
